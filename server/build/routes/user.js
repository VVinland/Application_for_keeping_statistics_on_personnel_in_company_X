import { Router } from 'express';
import userController from '../controllers/userController.js';
import { body } from 'express-validator';
import authMiddleware from '../middlewares/authMiddleware.js';
import registrationMiddleware from '../middlewares/registrationMiddlaware.js';
const router = Router();
router.post('/registration', registrationMiddleware, userController.registration);
router.post('/lastStepOfRegisteringEmployee', body('email').isEmail(), body('password').isLength({ min: 5, max: 32 }), userController.lastStepOfRegisteringEmployee);
router.post('/login', userController.login);
router.get('/salaryPayments/:value', userController.getSalaryPayments);
router.get('/getAllUsers', authMiddleware, userController.getAllUsers);
router.get('/refresh', userController.refresh);
router.get('/logout:id', userController.logout);
router.get('/numberHired/:value', userController.getNumberHired);
router.get('/numberDismissed/:value', userController.getNumberDismissed);
router.get('/birthdays/:value', userController.getBirthdays);
router.put('/employee', userController.updateUser);
router.delete('/employee', userController.deleteUser);
export default router;
